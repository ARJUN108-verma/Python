# Python program to demonstrate different data types

# Integer
x = 10
print("x =", x, "->", type(x))

# Float
y = 3.14
print("y =", y, "->", type(y))

# String
name = "Arjun"
print("name =", name, "->", type(name))

# Boolean
is_valid = True
print("is_valid =", is_valid, "->", type(is_valid))

# List
fruits = ["apple", "banana", "cherry"]
print("fruits =", fruits, "->", type(fruits))

# Tuple
coordinates = (10.0, 20.0)
print("coordinates =", coordinates, "->", type(coordinates))

# Dictionary
student = {"name": "Arjun", "age": 21}
print("student =", student, "->", type(student))

# Set
unique_numbers = {1, 2, 3, 3, 2}
print("unique_numbers =", unique_numbers, "->", type(unique_numbers))

# NoneType
nothing = None
print("nothing =", nothing, "->", type(nothing))


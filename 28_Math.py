import math

# Basic arithmetic operations
a = 10
b = 3

print("Basic Math Operations:")
print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
print(f"Modulus: {a} % {b} = {a % b}")
print(f"Exponentiation: {a} ** {b} = {a ** b}")
print()

# Using math module
print("Advanced Math Functions using math module:")
print(f"Square root of {a}: math.sqrt({a}) = {math.sqrt(a)}")
print(f"Power: math.pow({a}, {b}) = {math.pow(a, b)}")
print(f"Factorial of {b}: math.factorial({b}) = {math.factorial(b)}")
print(f"Log base e of {a}: math.log({a}) = {math.log(a)}")
print(f"Log base 10 of {a}: math.log10({a}) = {math.log10(a)}")
print(f"Sine of 90 degrees: math.sin(math.radians(90)) = {math.sin(math.radians(90))}")
print(f"Pi value: math.pi = {math.pi}")

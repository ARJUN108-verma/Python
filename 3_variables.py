# Program to show variables in Python

# Integer variable
age = 25
print("Age:", age)

# Float variable
height = 5.9
print("Height:", height)

# String variable
name = "Arjun"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# List variable
fruits = ["apple", "banana", "mango"]
print("Fruits:", fruits)

# Dictionary variable
person = {"name": "Arjun", "age": 25}
print("Person Dictionary:", person)

# Tuple variable
colors = ("red", "green", "blue")
print("Colors Tuple:", colors)

# Set variable
unique_numbers = {1, 2, 3}
print("Unique Numbers Set:", unique_numbers)


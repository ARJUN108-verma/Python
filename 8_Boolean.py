# Boolean values in Python
a = True
b = False

# Printing Boolean values
print("Value of a:", a)
print("Value of b:", b)

# Using Booleans in conditions
if a:
    print("a is True")
else:
    print("a is False")

if b:
    print("b is True")
else:
    print("b is False")

# Boolean expressions
x = 10
y = 5

print("Is x > y?", x > y)       # True
print("Is x < y?", x < y)       # False
print("Is x == y?", x == y)     # False
print("Is x != y?", x != y)     # True

# Using bool() function
print("bool(0):", bool(0))           # False
print("bool(1):", bool(1))           # True
print("bool('Hello'):", bool("Hello"))  # True
print("bool(''):", bool(""))         # False


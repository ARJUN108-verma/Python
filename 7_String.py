# Simple program to show string in Python

# Declare a string
my_string = "Hello, welcome to Python programming!"

# Print the string
print("The string is:", my_string)

# String operations
print("Uppercase:", my_string.upper())           # Convert to uppercase
print("Lowercase:", my_string.lower())           # Convert to lowercase
print("Length of string:", len(my_string))       # Length of string
print("First 5 characters:", my_string[:5])      # Slicing
print("Does it contain 'Python'? :", "Python" in my_string)  # Membership test

